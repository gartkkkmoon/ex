export function buildAnkrUrl(chainId, token = "") {
  const chain = SUPPORTED_CHAINS[chainId];
  if (!chain) {
    throw new Error(`Unsupported chain: ${chainId}`);
  }
  const suffix = token ? `/${token}` : "";
  return `https://rpc.ankr.com/${chain.ankrSlug}${suffix}`;
}

export function buildAnkrMultichainUrl(token = "") {
  const suffix = token ? `/${token}` : "";
  return `https://rpc.ankr.com/multichain${suffix}`;
}

export const EVM_CHAIN_IDS = Object.values(SUPPORTED_CHAINS)
  .filter((chain) => chain.rpcFamily === "evm")
  .map((chain) => chain.id);

export const TOKEN_API_CHAIN_IDS = Object.values(SUPPORTED_CHAINS)
  .filter((chain) => chain.tokenApiSlug)
  .map((chain) => chain.id);

export function tokenApiSlug(chainId) {
  const chain = SUPPORTED_CHAINS[chainId];
  if (!chain?.tokenApiSlug) throw new Error(`Unsupported Ankr Token API chain: ${chainId}`);
  return chain.tokenApiSlug;
}

